    <h1 class=" titulo-bonito text-center"  id="virus" > O que são e quais são os tipos de virus? </h1>
    <br>
    <div class="box">
        <p>Os vírus são programas de computador maliciosos projetados para se infiltrarem em sistemas de computador sem o consentimento do usuário e causarem danos, roubo de informações ou realizar outras atividades prejudiciais. Existem vários tipos de vírus, cada um com seus próprios métodos de propagação e efeitos. A lista a seguir conta com os tipos mais comuns de vírus:
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Vírus de Boot</h4>
        <p>Este tipo de vírus infecta a área de inicialização de um dispositivo de armazenamento, como o disco rígido ou uma unidade flash USB. Quando o sistema é inicializado a partir do dispositivo infectado, o vírus é ativado e pode se espalhar para outros dispositivos conectados.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Vírus de Arquivo</h4>
        <p> Esses vírus infectam arquivos executáveis, como programas ou scripts. Quando o arquivo infectado é executado, o vírus é ativado e pode se espalhar para outros arquivos executáveis no sistema.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Vírus de Macro</h4>
        <p> Os vírus de macro são incorporados em documentos que suportam macros, como documentos do Microsoft Word ou planilhas do Excel. Quando o documento infectado é aberto e as macros são ativadas, o vírus é executado e pode se espalhar para outros documentos.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Vírus de Script</h4>
        <p> Esses vírus são escritos em linguagens de script, como JavaScript ou VBScript, e são frequentemente transmitidos por e-mail ou sites maliciosos. Quando o script é executado, o vírus pode causar danos ao sistema ou se espalhar para outros computadores.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Vírus de Polimorfismo</h4>
        <p> Esses vírus são capazes de modificar sua própria estrutura ou código para evitar a detecção por software antivírus. Eles podem se alterar cada vez que infectam um novo sistema, tornando-os mais difíceis de detectar e remover.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Vírus de Cavalo de Troia (Trojan)</h4>
        <p> Os cavalos de Troia são programas que parecem ser benignos ou úteis, mas na realidade, eles contêm código malicioso que pode permitir a um invasor acessar e controlar o sistema infectado.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Worms (Vermes)</h4>
        <p> Ao contrário dos vírus, os vermes são programas autônomos que se espalham automaticamente para outros computadores em uma rede, geralmente explorando vulnerabilidades de segurança. Eles podem se propagar rapidamente e causar danos significativos à rede.
        <br>
        <br>
        <br>
        <p>Estes são apenas alguns exemplos dos muitos tipos de vírus que existem. À medida que os métodos de ataque cibernético evoluem, novas variantes de vírus e técnicas maliciosas continuam a surgir, destacando a importância de manter sistemas atualizados e usar medidas de segurança proativas para proteger contra ameaças cibernéticas.
    </div>
