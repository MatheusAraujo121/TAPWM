        <h1 class=" titulo-bonito text-center"  id="csp" > Como se proteger </h1>
    <br>
    <div class="box">
        <p>Proteger-se na web é fundamental para garantir a segurança de seus dados pessoais, financeiros e sensíveis. Estas são algumas medidas essenciais que você pode tomar para se proteger enquanto navega na internet:
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Use senhas fortes e únicas</h4>
        <p>Crie senhas complexas e diferentes para cada conta online que você possui. Evite usar informações pessoais óbvias e opte por combinações de letras, números e caracteres especiais. Considere o uso de gerenciadores de senhas para ajudar a lembrar e gerenciar suas credenciais de forma segura.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Ative a autenticação de dois fatores (2FA)</h4>
        <p>Além das senhas, habilite a autenticação de dois fatores sempre que possível. Isso adiciona uma camada extra de segurança, exigindo uma segunda forma de verificação, como um código enviado para o seu celular, para fazer login em suas contas.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Mantenha seu software atualizado</h4>
        <p>Certifique-se de manter seu sistema operacional, navegadores da web, aplicativos e programas antivírus sempre atualizados com as últimas correções de segurança. As atualizações frequentemente corrigem vulnerabilidades conhecidas que podem ser exploradas por hackers.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Tenha cuidado ao clicar em links</h4>
        <p>Evite clicar em links suspeitos ou desconhecidos em e-mails, mensagens instantâneas ou em sites não confiáveis. Verifique sempre a legitimidade dos links antes de clicar, especialmente se solicitarem informações pessoais ou financeiras.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Cuidado com phishing</h4>
        <p>Esteja ciente de e-mails, mensagens ou telefonemas fraudulentos que tentam enganá-lo para revelar informações confidenciais, como senhas ou números de cartão de crédito. Nunca forneça informações pessoais ou financeiras em resposta a solicitações suspeitas.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Use uma conexão segura</h4>
        <p>Ao acessar sites que exigem informações sensíveis, como dados bancários, certifique-se de que a conexão é segura. Procure pelo ícone de cadeado na barra de endereços do navegador e verifique se o URL começa com "https://" em vez de "http://".
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Faça backup regularmente</h4>
        <p>Faça backups regulares de seus arquivos importantes e dados para evitar a perda de informações em caso de ransomware, falha do sistema ou outro tipo de ataque cibernético.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Utilize software antivírus e firewall</h4>
        <p> Instale e mantenha atualizado um software antivírus confiável em seus dispositivos para protegê-los contra malware e outras ameaças. Além disso, ative e mantenha um firewall ativo para filtrar o tráfego de entrada e saída em sua rede.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Esteja ciente das configurações de privacidade</h4>
        <p>Revise e ajuste regularmente as configurações de privacidade em suas contas de redes sociais, serviços online e dispositivos para controlar quem pode ver suas informações e como elas são compartilhadas.
        <br>
        <br>
        <p><h4 class="text-center subtitulo">Eduque-se sobre segurança cibernética</h4>
        <p>Mantenha-se atualizado sobre as últimas ameaças cibernéticas e melhores práticas de segurança, participando de cursos, seminários ou lendo recursos confiáveis ​​sobre o assunto.
        <br>
        <br>
        <br>
        <p>Seguindo essas práticas de segurança e permanecendo vigilante enquanto navega na web, você pode reduzir significativamente o risco de se tornar uma vítima de atividades maliciosas online.
    </div>

